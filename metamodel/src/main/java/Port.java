public class Port {
    private int id;
    private String name;
    private boolean isAnalog;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isAnalog() {
        return isAnalog;
    }

    public void setAnalog(boolean isAnalog) {
        this.isAnalog = isAnalog;
    }

}
